digaunakan terhadap variable
 */

// contohnya
fun main() {
    // menggunakan operator (+) pada dua variable bertipe String
    val firstName = "Yaya"
    val lastName = "Fidel"
    println (firstName + " " +  lastName) // hasilnya adalah kedua buah string digabungkan

    // menggunakan operator (+) pada dua variable bertipe Int
    val angka_1 = 10
    val angka_2 = 15
    print ("Maka hasilnya : "+(angka_1 + angka_2)) //hasilnya adalah operasi tambah = 25
}